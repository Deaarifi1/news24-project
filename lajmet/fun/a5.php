        Krishtlindja është një festë fetare Krishtere. Të krishterët e festojnë çdo vit më 25 dhjetor ditëlindjen e Jezus Krishtit; në kishat e Lindjes festohet më 7 janar, për shkak të kalendarit Julian. Pas Pashkëve është festa më e rëndësishme për të krishterët. Rëndësia e ditës së Krishtlindjes nisi të rritet dalngadalë pas kurorëzimit të Karlit të Madh në vitin 800, ditën e Krishtlindjes. Sot dita e Krishtlindjes kremtohet me shfaqjen e skenës së lindjes së Krishtit, pemën e Krishtlindjes, shkëmbimin e dhuratave dhe Kartolinave, familjen e bashkuar, dhe ardhjen e Shën Nikollës apo Plakut të Vitit të Ri në prag të Krishtlindjes apo mëngjesin e Krishtlindjes. Krishtlindja është festa në të cilën të krishterët dëshirojnë më shumë se kurrë paqe e dashuri në botë. Datëlindja e Jezusit sipas të krishterëve besohet të jetë 24-25 dhjetori edhe pse ka dyshime për datën e saktë të lindjes pasi që ajo nuk përmendet në Dhiatën e Re. Kjo feste është ndër festat më të dashura dhe të preferuara nga mbarë bota. Pema e Krishtlindjeve Një nga zakonet më të përhapura të Krishtlindjeve sot është sjellja në shtëpi e pemës të krishtlindjeve dhe zbukurimi i tij. Nga vjen ky zakon? Coffin, në “Libri i Folklorit të Krishtlindjeve”, na tregon: “Shumica e njerëzve kanë dëgjuar që pema e Krishtlindjeve ka zënë fill në Tannenbaum dhe në njëfarë lloji të adhurimit gjerman të bimëve. Kjo është pjesërisht e vërtetë. Mirëpo, zakoni i përdorimit të bredhit dhe drurëve me gjelbërim të përjetshëm, në mënyrë ceremoniale, qe vendosur në Saturnalinë romake, madje edhe më herët në Egjipt” . Në traditën më të vjetër popullore, pjesa e poshtme e pemës së Krishtlindjeve ndizet (me drita) dhe qëndron e ndriçuar deri natën e Vitit të Ri dhe kjo tregon vlerën e zjarrit, imazhin e Diellit dhe konsumimin simbolik të trungut të pemës së vitit të vjeter me gjithë të këqijat që janë akumuluar në të. Sipas një tradite antike në disa vende aplikohet një ceremoni piktoreske me emrin e bukur “zjarrvënie”. Mbi trung hidhet verë e ëmbëlsira dhe ai digjet. Kjo simbolizon djegien e të këqijave dhe mëkatit. Pema e Krishtlindjeve është konsideruar si një kristianizim i traditës pagane dhe një ritual që shoqëron solsticin dimëror, që përfshin përdorimin e gjetheve të gjelbra dhe përshtatjen e pemës pagane.
